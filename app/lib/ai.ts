function hash(input: string) {
  let h = 2166136261;
  for (let i = 0; i < input.length; i++) {
    h ^= input.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function random(seed: number) {
  let t = seed;
  return () => {
    t += 0x6d2b79f5;
    let n = t;
    n = Math.imul(n ^ (n >>> 15), n | 1);
    n ^= n + Math.imul(n ^ (n >>> 7), n | 61);
    return ((n ^ (n >>> 14)) >>> 0) / 4294967296;
  };
}

function choose(items: string[], rng: () => number, recent: string) {
  const fresh = items.filter((item) => !recent.includes(item.slice(0, 14)));
  const pool = fresh.length ? fresh : items;
  return pool[Math.floor(rng() * pool.length)];
}

function detect(message: string, history: DialogueTurn[]): Insight {
  const recentUsers = history.filter((turn) => turn.role === "user").slice(-2).map((turn) => turn.text);
  const source = `${recentUsers.join(" ")} ${message}`;
  const topicScores = (Object.keys(topicPatterns) as Topic[])
    .filter((topic) => topic !== "general")
    .map((topic) => [topic, (source.match(new RegExp(topicPatterns[topic].source, "g")) || []).length] as const)
    .sort((a, b) => b[1] - a[1]);
  const topic = topicScores[0]?.[1] ? topicScores[0][0] : "general";
  const focus = focusPatterns.find(([, pattern]) => pattern.test(source))?.[0] || "uncertainty";
  const emotion =
    /害怕|恐惧|担心|紧张/.test(message) ? "fear" :
    /累|疲惫|没力气|撑不住/.test(message) ? "tired" :
    /难过|伤心|失落|痛苦|哭/.test(message) ? "sad" :
    /生气|愤怒|讨厌|不公平/.test(message) ? "angry" :
    /迷茫|不知道|纠结|困惑/.test(message) ? "confused" : "calm";
  const cleaned = message.replace(/[，。！？,.!?；;：:\n]/g, " ").replace(/\s+/g, " ").trim();
  const subjectMatch = cleaned.match(/(?:担心|害怕|纠结|不知道|觉得|想要|想|不想|为什么)(.{2,22})/);
  const subject = (subjectMatch?.[1] || cleaned).slice(0, 22);
  return { topic, focus, emotion, subject, isFollowUp: recentUsers.length > 0 };
}

function contextBridge(insight: Insight, rng: () => number) {
  if (!insight.isFollowUp || rng() < 0.42) return "";
  const options = [
    `你这次把问题推进了一点：现在关键不只是“${insight.subject}”，而是你准备怎样判断它。`,
    `接着前面的话看，真正反复出现的不是某个答案，而是你对“${insight.subject}”的不放心。`,
    `这不是一个全新的烦恼。它和前面那件事共用同一个结：你想先获得确定，才肯允许自己行动。`,
  ];
  return choose(options, rng, "");
}

function safetyReply() {
  return "我很在意你刚才说的危险。现在先不要独自承担，也先远离可能伤害自己的物品或地点。请立刻联系你信任的人，让对方陪着你；如果你可能马上伤害自己，请联系当地急救或报警服务。\n\n你不需要先把事情解释得漂亮。直接说：“我现在不安全，需要你陪我。” 如果愿意，请告诉我：你现在是独自一人吗？身边有没有可以马上联系的人？";
}

export async function generateReply(
  message: string,
  tone: Tone,
  variant: "normal" | "plain" | "sharp" = "normal",
  history: DialogueTurn[] = [],
): Promise<string> {
  // GitHub Pages currently uses this context-aware local generator.
  // For a real model, replace this adapter with a protected server-side request
  // carrying SYSTEM_PROMPT, tone and history. Never place an API key here.
  await new Promise((resolve) => setTimeout(resolve, 900 + Math.random() * 1000));

  if (/不想活|自杀|伤害自己|结束生命|活不下去/.test(message)) return safetyReply();

  const recentAssistant = history
    .filter((turn) => turn.role === "assistant")
    .slice(-4)
    .map((turn) => turn.text)
    .join("\n");
  const seed = hash(`${message}|${tone}|${variant}|${history.length}|${++generationNonce}|${Date.now()}`);
  const rng = random(seed);
  const insight = detect(message, history);
  const bridge = contextBridge(insight, rng);

  const opener = variant === "normal"
    ? choose(openings[tone], rng, recentAssistant)
    : choose(directLines[variant], rng, recentAssistant);
  const observation = choose(observations[insight.focus], rng, recentAssistant);
  const topicView = choose(topicViews[insight.topic], rng, recentAssistant);
  const metaphor = choose(metaphors[tone], rng, recentAssistant);
  const action = choose(actions[insight.topic], rng, recentAssistant);
  const question = choose(questions[insight.topic], rng, recentAssistant);

  if (variant === "plain") {
    return [opener, bridge, `${observation} ${topicView}`, `现在就做这个：${action}`, question]
      .filter(Boolean)
      .join("\n\n");
  }

  if (variant === "sharp") {
    const challenge = tone === "温和"
      ? "这不等于否定你的感受，只是不能让感受替全部事实签字。"
      : "不妨暂时取消自我辩护，也取消自我定罪，只看证据和代价。";
    return [opener, bridge, `${observation} ${topicView}`, challenge, action, question]
      .filter(Boolean)
      .join("\n\n");
  }

  const emotionalLine =
    insight.emotion === "tired" || insight.emotion === "sad"
      ? "所以先别要求自己马上振作。疲惫需要照料，问题才需要分析，两者不必互相冒充。"
      : insight.emotion === "angry"
        ? "生气说明某条边界可能被碰到了，但还要确认：被碰到的是事实、尊严，还是期待。"
        : "";

  return [
    opener,
    bridge,
    `${observation} ${topicView}`,
    tone === "荒诞" || rng() > 0.36 ? metaphor : emotionalLine,
    `可以先做一件不壮观但有用的事：${action}`,
    question,
  ].filter(Boolean).join("\n\n");
}
