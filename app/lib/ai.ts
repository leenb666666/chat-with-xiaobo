export async function generateReply(
  message: string,
  tone: Tone,
  variant: "normal" | "plain" | "sharp" = "normal",
): Promise<string> {
  // Production hook: replace this local adapter with a server-side request.
  // Send SYSTEM_PROMPT, tone and conversation history from a protected server route.
  // Never place provider API keys in this browser bundle.
  await new Promise((resolve) => setTimeout(resolve, 1250 + Math.random() * 900));

  if (/不想活|自杀|伤害自己|结束生命/.test(message)) {
    return "我很在意你刚才说的危险。现在先不要独自承担，也先远离可能伤害自己的物品或地点。请立刻联系你信任的人，让对方陪着你；如果你可能马上伤害自己，请联系当地急救或报警服务。\n\n你不需要先把事情解释得漂亮。只要直接说：“我现在不安全，需要你陪我。” 如果你愿意，也可以告诉我：你现在是独自一人吗？身边有没有可以马上联系的人？";
  }

  const matched = demos.find(([pattern]) => pattern.test(message));
  let reply =
    matched?.[1] ??
    `${toneOpeners[tone]}\n\n你说的困惑里，似乎有两件事缠在一起：事情本身不确定，以及你担心结果会替你定义“我是个怎样的人”。前者需要行动，后者往往只是大脑擅自成立的评审委员会，而且委员们通常脾气很坏。\n\n不妨把纸分成两栏：左边写今天能改变的，右边写现在无法控制的。只从左边挑一件，做二十分钟。人不一定要立刻想通全部生活，先把一个具体问题从混沌里捞出来，就已经是在夺回一点选择。\n\n你最怕的那个结果，究竟会带来什么实际变化？又有哪些只是你对自己的判词？`;

  if (variant === "plain") {
    reply = `直说吧：你现在不用把人生想明白，只要停止同时处理十件事。\n\n把最要紧的一件写下来，给它二十分钟。能做就做，不能做就找缺的条件。至于那些“如果失败我就完了”的念头，先别供着它们。结果会造成麻烦，但麻烦不等于判决。\n\n今天先完成一件你能控制的小事，然后吃饭、睡觉。道理可以明天再讲。`;
  }
  if (variant === "sharp") {
    reply = `说得尖锐一点：你可能正在拿“必须想清楚”拖延“先做一点”。这很常见，焦虑喜欢穿上深思熟虑的外套，坐在那里不动。\n\n别再审判整个人生。把问题缩小到一个今天能验证的动作：发一封邮件、读三页材料、拒绝一件不想做的事，或者认真睡一觉。行动未必立刻给答案，但会让那些凭空膨胀的恐惧失去一点体积。\n\n你要解决的是一个问题，不是证明自己配得上存在。`;
  }
  return reply;
}
